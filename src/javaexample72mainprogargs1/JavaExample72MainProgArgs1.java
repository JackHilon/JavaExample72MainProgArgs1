
package javaexample72mainprogargs1;


public class JavaExample72MainProgArgs1 {

    
    public static void main(String[] args) {
        
        System.out.println("The number of args arguments: "+args.length);
        
        System.out.println("The arguments....");
        for (int i = 0; i < args.length; i++) {
            System.out.println(args[i]);
        }
        
        
        
        
    }
    
}
